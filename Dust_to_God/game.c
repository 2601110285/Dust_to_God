void selectDifficulty() { // [기능] 게임을 시작하기 전 난이도를 고르게 합니다.
    system("cls"); // [화면] 화면을 깨끗하게 지웁니다.
    setColor(11); // [출력] 하늘색으로 변경합니다.
    printf("\n\n\t====================================\n"); // [출력] 테두리 윗부분.
    printf("\t=        난 이 도  선 택           =\n"); // [출력] 난이도 선택 제목.
    printf("\t====================================\n\n"); // [출력] 테두리 아랫부분.
    setColor(10); printf("\t          [1] 하 (빠른 성장)\n"); // [출력] 초록색(10번)으로 쉬운 난이도 표시.
    setColor(14); printf("\t          [2] 중 (일반 모드)\n"); // [출력] 노란색(14번)으로 보통 난이도 표시.
    setColor(12); printf("\t          [3] 상 (포식자 가속)\n"); // [출력] 빨간색(12번)으로 어려운 난이도 표시.

    setColor(7); // [출력] 기본 회색으로 복귀.
    printf("\n\t   원하는 난이도의 숫자 키를 누르세요."); // [출력] 입력을 유도합니다.

    int key = 0; // [저장] 누른 키를 담을 변수를 0으로 초기화합니다.
    while (key < '1' || key > '3') { // [조건] 1, 2, 3이 아닌 다른 키를 누르면 계속 반복해서 다시 입력받습니다.
        if (_kbhit()) key = _getch(); // [입력] 키가 눌렸다면 그 키값을 가져와서 확인합니다.
    } // [종료] 올바른 숫자를 누를 때까지 갇히는 루프 끝.
    difficultyMult = key - '0'; // [변화] 문자 '1'을 실제 숫자 1로 바꾸어 난이도 변수에 저장합니다. ('1' - '0' = 1)
    targetMass = BASE_MASS_STAGE2 * difficultyMult; // [변화] 2단계 목표 점수를 (기본값 * 난이도 배수)로 계산해 세팅합니다.

    system("cls"); // [화면] 선택이 끝났으니 화면을 지우고 본 게임을 준비합니다.
} // [종료] 난이도 선택 함수 끝.

int askRestart() { // [기능] 게임오버나 클리어 후 다시 시작할지 묻습니다.
    setColor(14); // [출력] 노란색 글씨로 눈에 띄게 합니다.
    printf("\n\t 다시 플레이하시겠습니까? (R: 재시작 / ESC: 종료)\n"); // [출력] R이나 ESC를 누르라고 안내합니다.
    setColor(7); // [출력] 다시 기본 색으로 되돌립니다.

    while (1) { // [반복] 올바른 키를 누를 때까지 대기합니다.
        if (_kbhit()) { // [조건] 키보드 입력이 감지되면?
            int key = _getch(); // [입력] 무슨 키를 눌렀는지 가져옵니다.
            if (key == 'r' || key == 'R') return 1; // [출력] 대소문자 R을 눌렀다면 1(재시작)을 반환합니다.
            if (key == KEY_ESC) return 0; // [출력] ESC(27번)를 눌렀다면 0(종료)을 반환합니다.
        } // [종료] 키 입력 감지 끝.
    } // [종료] 대기 무한 루프 끝.
} // [종료] 재시작 질문 함수 끝.

