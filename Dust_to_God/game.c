void setCursorPosition(int x, int y) { // [기능] X, Y 좌표를 받아 콘솔 화면의 커서를 이동시킵니다.
    COORD coord = { (SHORT)x, (SHORT)y }; // [변환] 입력받은 숫자를 윈도우 시스템이 이해하는 좌표계 형태로 바꿉니다.
    SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), coord); // [출력] 실제 윈도우 화면의 커서를 그 위치로 순간이동시킵니다.
} // [종료] 커서 이동 함수 끝.

void hideCursor() { // [기능] 거슬리는 커서(글자를 칠 때 깜빡이는 선)를 안 보이게 합니다.
    CONSOLE_CURSOR_INFO ci = { 100, FALSE }; // [변환] 커서 크기는 100, 보이는 상태는 FALSE(거짓)로 설정합니다.
    SetConsoleCursorInfo(GetStdHandle(STD_OUTPUT_HANDLE), &ci); // [출력] 이 설정을 윈도우 시스템에 적용해 커서를 숨깁니다.
} // [종료] 커서 숨김 함수 끝.

void setColor(int code) { // [기능] 숫자를 입력받아 앞으로 출력될 글자의 색상을 변경합니다.
    SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), code); // [출력] 윈도우 명령어를 통해 콘솔 텍스트 색상을 입력한 번호로 바꿉니다.
} // [종료] 색상 변경 함수 끝.

