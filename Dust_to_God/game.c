void initMap(int stage) { // [기능] 스테이지가 바뀔 때 맵 공간과 정보를 새롭게 세팅합니다.
    int i, j; // [선언] 배열을 탐색할 때 쓸 반복문용 변수입니다.
    for (i = 0; i < ROWS; i++) { // [반복] 세로줄(20줄)을 한 줄씩 훑습니다.
        for (j = 0; j < COLS; j++) map[i][j] = ' '; // [변화] 가로줄(40칸)을 돌며 모든 칸을 공백(' ')으로 지워버립니다.
    } // [종료] 2차원 배열 맵 초기화 끝.
    playerRow = ROWS / 2; playerCol = COLS / 2; // [변화] 플레이어의 위치를 맵의 정중앙(세로 절반, 가로 절반)으로 옮깁니다.

    bhRow = -1; bhCol = -1; // [변화] 블랙홀의 위치를 맵 밖(-1)으로 보내서 일단 안 보이게 숨깁니다.
    currentStage = stage; // [변화] 현재 스테이지 정보를 전달받은 새 스테이지 번호로 바꿉니다.
    turnCount = 0; // [변화] 턴 카운터(시간)를 0으로 초기화합니다.

    if (stage == STAGE_1) { // [조건] 만약 1단계라면?
        targetMass = BASE_MASS_STAGE2 * difficultyMult; // [변화] 2단계로 가기 위한 목표 점수를 설정합니다.
        for (i = 0; i < 80; i++) map[rand() % ROWS][rand() % COLS] = ','; // [변화] 우주 맵의 무작위 위치에 자잘한 먼지(',') 80개를 뿌립니다.
    } // [종료] 1단계 세팅 끝.
    else if (stage == STAGE_2) { // [조건] 만약 2단계라면?
        bhRow = 1; bhCol = 1; // [변화] 좌측 상단 구석(1,1)에 블랙홀을 등장시킵니다!
        targetMass = BASE_MASS_STAGE3 * difficultyMult; // [변화] 3단계로 가기 위한 목표 점수를 세팅합니다.
        for (i = 0; i < 40; i++) map[rand() % ROWS][rand() % COLS] = '*'; // [변화] 1단계보다 크고 맛있는 별('*') 40개를 무작위로 뿌립니다.
    } // [종료] 2단계 세팅 끝.
    else if (stage == STAGE_3) { // [조건] 만약 3단계라면?
        bhRow = 1; bhCol = 1; // [변화] 역시 블랙홀을 구석에서 스폰시킵니다.
        targetMass = BASE_MASS_STAGE4 * difficultyMult; // [변화] 4단계로 가기 위한 더 높은 목표 점수를 세팅합니다.
        for (i = 0; i < 15; i++) map[rand() % ROWS][rand() % COLS] = 'G'; // [변화] 점수를 많이 주는 거대 은하('G') 15개를 뿌립니다.
    } // [종료] 3단계 세팅 끝.
    else if (stage == STAGE_4) { // [조건] 마지막 4단계라면?
        bhRow = 1; bhCol = 1; // [변화] 블랙홀이 여전히 쫓아오게 구석에 스폰합니다.
        targetMass = BASE_MASS_ENDING * difficultyMult; // [변화] 대망의 엔딩을 보기 위한 최종 목표 점수를 설정합니다.
        for (i = 0; i < 15; i++) map[rand() % ROWS][rand() % COLS] = 'S'; // [변화] 최고급 영양분인 초거대 항성('S')을 15개 뿌립니다.
    } // [종료] 4단계 세팅 끝.
} // [종료] 맵 초기화 함수 끝.

void spawnContinuousItems() { // [기능] 게임 중간중간 먹이가 마르지 않게 하늘에서 계속 뿌려주는 함수입니다.
    if (currentStage == MINIGAME_1) return; // [조건] 미니게임 중일 때는 먹이가 생기면 안 되므로 그냥 함수를 종료합니다.

    int r = rand() % ROWS; // [추출] 0부터 세로 끝-1 사이의 무작위 숫자를 뽑습니다 (랜덤 Y).
    int c = rand() % COLS; // [추출] 0부터 가로 끝-1 사이의 무작위 숫자를 뽑습니다 (랜덤 X).
    if (map[r][c] == ' ' && !(r == playerRow && c == playerCol)) { // [조건] 뽑은 곳이 빈칸이고, 내 캐릭터 위가 아니라면?
        if (currentStage == STAGE_1 && turnCount % 3 == 0) map[r][c] = ','; // [변화] 1단계에선 매 3턴마다 그 위치에 먼지(',')를 만듭니다.
        else if (currentStage == STAGE_2 && turnCount % 6 == 0) map[r][c] = '*'; // [변화] 2단계에선 매 6턴마다 그 위치에 별('*')을 만듭니다.
        else if (currentStage == STAGE_3 && turnCount % 8 == 0) map[r][c] = 'G'; // [변화] 3단계에선 매 8턴마다 은하('G')를 만듭니다.
        else if (currentStage == STAGE_4 && turnCount % 10 == 0) map[r][c] = 'S'; // [변화] 4단계에선 매 10턴마다 항성('S')을 만듭니다. 단계가 오를수록 생성 주기가 느려집니다.
    } // [종료] 빈칸 확인 후 생성 조건문 끝.
} // [종료] 먹이 자동 생성 함수 끝.

void updateBlackHoleChase() { // [기능] 적(블랙홀)이 플레이어를 향해 다가오게 하는 인공지능(?) 로직입니다.
    if (currentStage < STAGE_2 || currentStage > STAGE_4) return; // [조건] 1단계거나 이벤트 중이면 블랙홀이 안 나오므로 바로 종료합니다.

    int moveDelay = 5 - difficultyMult; // [변화] 난이도(1~3)를 5에서 빼서, 블랙홀이 몇 턴마다 움직일지(4~2턴) 결정합니다. 난이도가 높을수록 숫자가 작아져 빨리 움직입니다.
    if (turnCount % moveDelay == 0) { // [조건] 계산된 딜레이 턴수가 찰 때마다 움직이게 합니다.
        if (bhRow < playerRow) bhRow++; else if (bhRow > playerRow) bhRow--; // [변화] 블랙홀이 플레이어보다 위에 있으면 아래로, 아래에 있으면 위로 1칸 이동합니다.
        if (bhCol < playerCol) bhCol++; else if (bhCol > playerCol) bhCol--; // [변화] 블랙홀이 플레이어보다 왼쪽에 있으면 오른쪽으로, 오른쪽에 있으면 왼쪽으로 1칸 이동합니다.
    } // [종료] 기본 추격 로직 끝.

    int dist = abs(bhRow - playerRow) + abs(bhCol - playerCol); // [계산] 나와 블랙홀의 X, Y 좌표 차이를 절대값(abs)으로 더해 총 거리를 잽니다.
    if (dist <= 4 && turnCount % 2 == 0) { // [조건] 거리가 4칸 이하로 아주 가까워지고 짝수 턴이라면? (블랙홀의 강한 인력 발동)
        if (playerRow < bhRow) playerRow++; else if (playerRow > bhRow) playerRow--; // [변화] 내 캐릭터가 강제로 블랙홀 쪽으로 1칸 끌려갑니다!
        if (playerCol < bhCol) playerCol++; else if (playerCol > bhCol) playerCol--; // [변화] 가로 방향으로도 블랙홀 쪽으로 1칸 끌려갑니다.
    } // [종료] 블랙홀 인력(끌어당김) 로직 끝.

    if (dist <= 1) { // [조건] 블랙홀과 내 거리가 1칸 이하로 닿아버렸다면?
        playerMass -= (difficultyMult * 2); // [변화] 내 소중한 질량(점수)이 깎입니다. 난이도에 비례해서 더 아프게 깎입니다.
        if (playerMass <= 0) isGameOver = 1; // [조건] 깎이다가 점수가 0 이하가 되면 파멸하며 isGameOver 플래그를 올려 게임오버 시킵니다.
    } // [종료] 충돌 판정 및 데미지 로직 끝.
} // [종료] 블랙홀 추적 함수 끝.

void drawMap() { // [기능] 컴퓨터 메모리에 있는 배열 맵을 우리가 볼 수 있게 화면에 그려줍니다.
    setCursorPosition(0, 0); // [화면] 화면 깜빡임을 없애기 위해 커서를 지우지 않고 맨 위 좌측(0,0)으로 보냅니다.
    setColor(8); // [출력] 어두운 회색으로 맵의 윗면 테두리를 설정합니다.
    for (int j = 0; j < COLS + 2; j++) printf("-"); // [출력] 맵 가로 너비보다 2칸 넓게 가로 테두리 선을 쭉 그립니다.
    printf("\n"); // [출력] 테두리를 그리고 줄 바꿈을 합니다.

    for (int i = 0; i < ROWS; i++) { // [반복] 맵의 세로줄을 맨 위부터 한 줄씩 그립니다.
        setColor(8); printf("|"); // [출력] 줄 시작마다 왼쪽 세로 테두리 벽(|)을 칩니다.
        for (int j = 0; j < COLS; j++) { // [반복] 가로줄을 왼쪽부터 한 칸씩 그립니다.
            if (i == playerRow && j == playerCol) { // [조건] 지금 그리는 칸이 내 캐릭터의 위치라면?
                if (currentStage == MINIGAME_1) { setColor(14); printf("^"); } // [출력] 미니게임 중이면 비행기 모양(^)으로 노랗게 그립니다.
                else { setColor(11); printf("P"); } // [출력] 일반 게임 중이면 하늘색 알파벳 P로 그립니다.
            } // [종료] 플레이어 그리기 끝.
            else if (i == bhRow && j == bhCol && currentStage >= STAGE_2 && currentStage <= STAGE_4) { // [조건] 지금 그리는 칸이 블랙홀 위치이고, 블랙홀이 등장하는 단계라면?
                setColor(13); printf("@"); // [출력] 보라색(13번) 기호 @ 로 섬뜩하게 그립니다.
            } // [종료] 블랙홀 그리기 끝.
            else { // [조건] 캐릭터도 적도 아닌 일반 맵이나 아이템이라면?
                char c = map[i][j]; // [추출] 배열에서 해당 칸의 문자 데이터를 꺼냅니다.
                if (c == ',') setColor(8); // [색상] 먼지는 눈에 덜 띄는 회색으로 설정.
                else if (c == '*') setColor(14); // [색상] 별은 반짝이는 노란색으로 설정.
                else if (c == 'G') setColor(5); // [색상] 은하는 신비로운 자주색으로 설정.
                else if (c == 'S') setColor(7); // [색상] 항성은 밝은 회색으로 설정.
                else if (c == '#') setColor(12); // [색상] 미니게임의 운석은 위험한 빨간색으로 설정.
                printf("%c", c); // [출력] 결정된 색상으로 해당 문자를 화면에 출력합니다 (빈칸이면 공백 출력).
            } // [종료] 아이템 및 빈칸 그리기 끝.
        } // [종료] 한 가로줄 그리기 반복문 끝.
        setColor(8); printf("|\n"); // [출력] 가로줄이 끝나면 오른쪽 세로 테두리 벽(|)을 치고 줄을 바꿉니다.
    } // [종료] 세로줄 20칸 그리기 완료.
    for (int j = 0; j < COLS + 2; j++) printf("-"); // [출력] 맵 아랫면 테두리 선을 쭉 그립니다.
    printf("\n"); // [출력] 테두리 끝 줄바꿈.
    setColor(7); // [출력] 다음 출력을 위해 색상을 원래대로 복구합니다.
} // [종료] 맵 화면 그리기 함수 끝.

void drawHUD() { // [기능] 게임 화면 바로 아래에 내 점수와 상태바(Head-Up Display)를 표시합니다.
    setColor(11); printf("\n === 우주 스케일: "); // [출력] 상태바의 제목을 예쁘게 출력합니다.
    setColor(15); // [출력] 내용을 밝은 흰색으로 바꿉니다.
    if (currentStage == STAGE_1) printf("우주 먼지"); // [출력] 1단계일 때의 계급을 출력.
    else if (currentStage == STAGE_2) printf("원시별 (추격자 발생!)"); // [출력] 2단계일 때 경고와 함께 출력.
    else if (currentStage == STAGE_3) printf("원시 은하"); // [출력] 3단계 계급 출력.
    else if (currentStage == STAGE_4) printf("나선 은하"); // [출력] 4단계 계급 출력.

    setColor(11); printf(" ===\n"); // [출력] 장식 마무리.
    setColor(7); printf(" 질량: %d / %d  |  ", playerMass, targetMass); // [출력] 내 현재 점수와 다음 단계로 갈 목표 점수를 숫자로 보여줍니다.
    if (currentStage >= STAGE_2 && currentStage <= STAGE_4) { // [조건] 블랙홀이 쫓아오고 있는 상태라면?
        setColor(12); printf("블랙홀과의 거리: %d", abs(bhRow - playerRow) + abs(bhCol - playerCol)); // [출력] 살떨리게 적과의 현재 거리를 빨간색으로 보여줍니다.
    } // [종료] 블랙홀 거리 출력 끝.
    printf("\n"); // [출력] 상태바를 다 쓰고 줄을 바꿉니다.
} // [종료] 상태바 출력 함수 끝.

void processInput() { // [기능] 사용자가 키보드를 눌렀을 때 내 캐릭터(P)를 움직이고 아이템을 먹습니다.
    if (_kbhit()) { // [조건] '지금 이 순간 키보드가 눌려있는가?' 확인합니다.
        int key = _getch(); // [입력] 눌려있다면 그 키를 꺼내옵니다.
        if (key == KEY_SPECIAL) { // [조건] 방향키는 값이 2번(224 누른 후 화살표 방향) 연속으로 들어오므로, 특수키 224가 먼저 들어왔는지 봅니다.
            key = _getch(); // [입력] 진짜 화살표 방향 값을 두 번째로 꺼내옵니다.
            if (key == KEY_UP && playerRow > 0) playerRow--; // [변화] 위 방향키고, 맨 위 벽이 아니라면 Y좌표를 1 빼서 위로 올립니다.
            if (key == KEY_DOWN && playerRow < ROWS - 1) playerRow++; // [변화] 아래 방향키고, 맨 아래 벽이 아니라면 Y좌표를 1 더해 아래로 내립니다.
            if (key == KEY_LEFT && playerCol > 0) playerCol--; // [변화] 왼쪽 방향키고, 왼쪽 끝 벽이 아니라면 X좌표를 1 빼서 왼쪽으로 갑니다.
            if (key == KEY_RIGHT && playerCol < COLS - 1) playerCol++; // [변화] 오른쪽 방향키고, 오른쪽 끝 벽이 아니라면 X좌표를 1 더해 오른쪽으로 갑니다.
        } // [종료] 캐릭터 방향키 이동 로직 끝.

        if (currentStage != MINIGAME_1) { // [조건] 운석 피하기 미니게임 중이 아니라면 (아이템을 먹을 수 있다면)?
            char item = map[playerRow][playerCol]; // [추출] 캐릭터가 이동한 칸(배열)에 뭐가 있는지 가져옵니다.
            if (item == ',') { playerMass += 1; map[playerRow][playerCol] = ' '; } // [변화] 먼지면 점수 1점을 올리고, 그 칸을 빈칸으로 지워버립니다. (먹방 성공)
            else if (item == '*') { playerMass += 2; map[playerRow][playerCol] = ' '; } // [변화] 별이면 점수 2점을 올리고 맵에서 지웁니다.
            else if (item == 'G') { playerMass += 5; map[playerRow][playerCol] = ' '; } // [변화] 은하면 점수 5점을 올리고 맵에서 지웁니다.
            else if (item == 'S') { playerMass += 10; map[playerRow][playerCol] = ' '; } // [변화] 항성이면 점수 10점을 올리고 맵에서 지웁니다.
        } // [종료] 아이템 획득 로직 끝.
    } // [종료] 키보드 감지 시 동작 끝.
} // [종료] 입력 처리 함수 끝.

