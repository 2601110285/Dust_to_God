void runMiniGame1() { // [기능] 3단계를 깨면 나오는 하늘에서 떨어지는 운석 피하기 게임입니다.
    system("cls"); setColor(12); // [화면] 화면을 지우고 붉은색 경고 빛을 냅니다.
    printf("\n\n\t[ 미니게임: 거대 운석 지대 돌파 ]\n"); // [출력] 미니게임 진입을 알립니다.
    setColor(7); // [출력] 회색 글씨로 복구.
    printf("\t블랙홀을 따돌리기 위해 운석 지대를 통과해야 합니다.\n"); // [출력] 스토리텔링 부여.
    printf("\t경고: 운석('#')에 한 번이라도 스치면 즉시 파괴됩니다!\n"); // [출력] 규칙(스치면 사망) 설명.
    Sleep(3000); // [대기] 플레이어가 마음의 준비를 할 수 있게 3초(3000ms)간 멈춥니다.

    system("cls"); // [화면] 설명 화면 지우기.

    bhRow = -1; bhCol = -1; // [변화] 미니게임 중에는 블랙홀이 안 따라오게 멀리 치워버립니다.

    currentStage = MINIGAME_1; // [변화] 시스템 상태를 미니게임 모드(91)로 바꿉니다.
    for (int i = 0; i < ROWS; i++) { // [반복] 맵 전체 세로 훑기.
        for (int j = 0; j < COLS; j++) map[i][j] = ' '; // [변화] 미니게임을 위해 맵 전체를 빈칸으로 싹 지웁니다.
    } // [종료] 맵 비우기 끝.
    playerRow = ROWS - 2; // [변화] 플레이어의 위치를 맨 아래쪽(바닥에서 2칸 위)으로 옮깁니다. (갤러그처럼)
    playerCol = COLS / 2; // [변화] 가로 위치는 정중앙으로 세팅합니다.

    int miniTurn = 0; // [변화] 미니게임 전용 타이머(턴)를 0으로 맞춥니다.
    int maxTurns = 120; // [설정] 120번의 턴을 버티면 승리하는 것으로 목표를 정합니다.
    int dropDelay = 4 - difficultyMult; // [설정] 난이도가 높을수록 떨어지는 속도(딜레이)가 짧아져 운석이 자주 떨어집니다.

    while (miniTurn < maxTurns && !isGameOver) { // [반복] 120턴을 못 버텼고, 아직 안 죽었다면 계속 게임을 돌립니다.
        processInput(); // [호출] 내 캐릭터를 좌우로 움직일 수 있게 키보드 입력을 처리합니다.

        if (miniTurn % dropDelay == 0) { // [조건] 떨어질 시간이 되었다면? (전체 맵을 한 칸씩 아래로 내립니다)
            for (int i = ROWS - 1; i > 0; i--) { // [반복] 맵의 맨 아랫줄부터 시작해 위로 거꾸로 올라가며 살핍니다.
                for (int j = 0; j < COLS; j++) { // [반복] 각 가로줄의 모든 칸을 훑습니다.
                    map[i][j] = map[i - 1][j]; // [변화] 윗줄에 있던 내용(운석이나 빈칸)을 아래쪽 칸으로 복사해 내립니다. (비가 오는 효과)
                } // [종료] 가로 한 줄 내리기 끝.
            } // [종료] 맵 전체 내리기 완료.
            for (int j = 0; j < COLS; j++) map[0][j] = ' '; // [변화] 맵이 한 칸씩 내려왔으니, 맨 윗줄(0번 줄)은 깨끗하게 빈칸으로 비워줍니다.
            int meteorCount = difficultyMult * 2; // [계산] 맨 윗줄에 새로 생성할 운석 개수를 난이도에 비례해서 정합니다.
            for (int k = 0; k < meteorCount; k++) { // [반복] 계산된 운석 개수만큼 반복합니다.
                map[0][rand() % COLS] = '#'; // [변화] 맨 윗줄의 무작위 위치에 새 운석('#')을 생성합니다.
            } // [종료] 새 운석 뿌리기 끝.
        } // [종료] 운석 비 내리기 로직 끝.

        if (map[playerRow][playerCol] == '#') { // [조건] 하늘에서 내려온 맵을 확인해보니 내 캐릭터 머리 위에 운석('#')이 있다면?!
            isGameOver = 1; // [변화] 즉시 사망 판정을 내립니다.
        } // [종료] 충돌 판정 끝.

        drawMap(); // [호출] 운석이 내려오고 내 캐릭터가 움직인 새 맵의 상태를 화면에 그립니다.
        setColor(14); printf("\n\t=== [ 미니게임: 운석 지대 돌파 ] ===\n"); // [출력] 상태바 제목을 그립니다.
        setColor(7);  printf("\t남은 돌파 거리: %d / %d\n", maxTurns - miniTurn, maxTurns); // [출력] 120턴 중 몇 턴 남았는지 거리로 표현해 보여줍니다.
        setColor(12); printf("\t경고: 운석에 닿으면 즉시 파멸합니다!\n"); // [출력] 경고 문구를 띄웁니다.

        miniTurn++; // [변화] 턴(시간)이 1 흘러갑니다.
        Sleep(40); // [대기] 본 게임보다 조금 더 빠른 0.04초 간격으로 프레임을 넘겨 긴박감을 줍니다.
    } // [종료] 미니게임 120턴 무한 루프 끝.

    if (!isGameOver) { // [조건] 무한 루프를 빠져나왔는데 아직 살아있다면? (120턴 생존 성공)
        system("cls"); setColor(10); // [화면] 화면 지우고 초록색 성공 빛을 냅니다.
        printf("\n\t운석 지대를 무사히 돌파했습니다!\n"); // [출력] 돌파 성공 메시지를 띄웁니다.
        Sleep(2000); // [대기] 성취감을 느낄 수 있게 2초간 대기.
        initMap(STAGE_4); // [호출] 보상으로 최종장인 4단계 맵으로 플레이어를 안내합니다.
    } // [종료] 미니게임 클리어 로직 끝.
} // [종료] 미니게임 전체 함수 끝.

