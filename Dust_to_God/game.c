void playEndingAnimation() { // [기능] 게임을 깼을 때 화려하게 퍼져나가는 빅뱅 이펙트를 만듭니다.
    int r, i, j; // [선언] 거리(r)와 화면 좌표를 계산할 변수들입니다.
    int centerRow = ROWS / 2; // [설정] 맵의 세로 중심점을 찾습니다.
    int centerCol = COLS / 2; // [설정] 맵의 가로 중심점을 찾습니다.

    system("cls"); // [화면] 화면을 깨끗이 비웁니다.

    system("color F0"); // [화면] 윈도우 배경색 전체를 하얗게 번쩍이게 만듭니다 (폭발 섬광).
    Sleep(80); // [대기] 0.08초 동안 유지.
    system("color 0F"); // [화면] 다시 까만 우주 배경으로 되돌려 눈뽕 효과를 줍니다.
    Sleep(50); // [대기] 0.05초 대기.

    for (r = 1; r <= 35; r += 2) { // [반복] 폭발의 반지름(r)을 1부터 35까지 2씩 팍팍 늘려가며 그립니다.
        setCursorPosition(0, 0); // [화면] 커서를 맨 위로 올려 화면을 다시 그릴 준비를 합니다.
        for (i = 0; i < ROWS; i++) { // [반복] 세로줄을 한 줄씩 훑습니다.
            for (j = 0; j < COLS; j++) { // [반복] 가로줄을 한 칸씩 훑습니다.
                int dist = (abs(i - centerRow) * 2) + abs(j - centerCol); // [계산] 현재 칸이 중심폭발지점에서 얼마나 떨어져 있는지 다이아몬드 형태로 거리를 잽니다 (가로 칸이 좁아서 *2 보정).

                if (dist == r || dist == r + 1) { setColor(15); printf("*"); } // [출력] 폭발의 맨 바깥쪽 가장자리면 가장 밝은 흰색 별을 찍습니다.
                else if (dist == r - 1 || dist == r - 2) { setColor(14); printf("O"); } // [출력] 폭발 중간층이면 노란 불덩이(O)를 찍습니다.
                else if (dist == r - 3 || dist == r - 4) { setColor(12); printf("."); } // [출력] 폭발 안쪽층이면 붉은 찌꺼기(.)를 찍습니다.
                else { // [조건] 폭발이 이미 휩쓸고 간 아예 안쪽 빈 공간이라면?
                    if (dist < r - 4 && rand() % 15 == 0) { setColor(11); printf("+"); } // [출력] 15분의 1 확률로 잔해 반짝임(+)을 파랗게 그립니다.
                    else if (dist < r - 4 && rand() % 25 == 0) { setColor(13); printf("."); } // [출력] 25분의 1 확률로 우주 먼지(.)를 보라색으로 그립니다.
                    else printf(" "); // [출력] 나머지는 그냥 텅 빈 검은 우주로 놔둡니다.
                } // [종료] 거리별 폭발 잔해 그리기 끝.
            } // [종료] 가로 훑기 끝.
            printf("\n"); // [출력] 줄 바꿈.
        } // [종료] 세로 훑어 전체 화면 그리기 끝.
        Sleep(15); // [대기] 폭발이 퍼져나가는 속도를 아주 빠르게(0.015초) 보여줍니다.
    } // [종료] 폭발 반지름 넓히기 루프 끝.

    Sleep(1000); // [대기] 여운을 즐기도록 1초 멈춤.
    drawSupermassiveGalaxy(); // [호출] 진정한 엔딩 그림인 초거대 은하수를 화면에 띄웁니다.
} // [종료] 빅뱅 이펙트 함수 끝.

